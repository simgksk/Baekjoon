#include <iostream>
#include <string>

using namespace std;

int main()
{
	int t;
	string s;

	cin >> t;

	while (t--)
	{
		cin >> s;

		if (s.length() > 2)
			cout << s.front() << s.back() << "\n";
		else if (s.length() == 2)
			cout << s << "\n";
		else if (s.length() == 1)
			cout << s << s << "\n";
	}

	return 0;
}
