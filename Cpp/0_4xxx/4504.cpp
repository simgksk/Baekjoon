#include <iostream>
#include <string>

using namespace std;

int main()
{
#pragma region 4504. 배수 찾기

	int n, num;

	cin >> n;

	while (true)
	{
		cin >> num;

		if (num == 0)
			break;

		if (num % n == 0)
			cout << num << " is a multiple of " << n << ".\n";
		else
			cout << num << " is NOT a multiple of " << n << ".\n";
	}

#pragma endregion

	return 0;
}
